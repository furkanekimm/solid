package com.ba.solid.openclosedprinciple.badexample;

public class Rectangle {
    private Long Width;
    private Long Height;

    public Long getWidth() {
        return Width;
    }

    public void setWidth(Long width) {
        Width = width;
    }

    public Long getHeight() {
        return Height;
    }

    public void setHeight(Long weight) {
        Height = weight;
    }
}


